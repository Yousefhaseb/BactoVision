# دالة ال PDF المحدثة (مضادة للانهيار)
def create_ast_pdf(patient_name, sample_id, bacteria_name, report_data):
    pdf = FPDF()
    pdf.add_page()
    
    # دالة صغيرة لتنظيف أي نص من الحروف العربي أو الإيموجيز عشان FPDF ميضربش
    def clean_text(text):
        if text is None:
            return ""
        return str(text).encode('latin-1', 'ignore').decode('latin-1')

    # تنظيف المدخلات
    patient_name_clean = clean_text(patient_name)
    if not patient_name_clean and patient_name: 
        # لو كتب الاسم كله عربي واتفلتر، هنكتب الجملة دي بدل ما نسيبها فاضية
        patient_name_clean = "Arabic Name (Unsupported in PDF)"
    elif not patient_name_clean:
        patient_name_clean = "Unknown Patient"
        
    sample_id_clean = clean_text(sample_id)
    bacteria_name_clean = clean_text(bacteria_name)

    # 1. Header (Lab Name & Logo text)
    pdf.set_font("Arial", 'B', 16)
    pdf.cell(0, 10, "BactoVision(TM) - Clinical Laboratory Report", ln=True, align='C')
    pdf.set_font("Arial", '', 10)
    pdf.cell(0, 10, "Powered by SCOPE AI", ln=True, align='C')
    pdf.line(10, 30, 200, 30)

    # 2. Patient & Sample Info
    pdf.ln(10)
    pdf.set_font("Arial", 'B', 11)
    pdf.cell(35, 8, "Patient Name:", 0, 0)
    pdf.set_font("Arial", '', 11)
    pdf.cell(85, 8, patient_name_clean, 0, 0)

    pdf.set_font("Arial", 'B', 11)
    pdf.cell(30, 8, "Date:", 0, 0)
    pdf.set_font("Arial", '', 11)
    pdf.cell(40, 8, datetime.now().strftime("%Y-%m-%d %H:%M"), 0, 1)

    pdf.set_font("Arial", 'B', 11)
    pdf.cell(35, 8, "Sample ID:", 0, 0)
    pdf.set_font("Arial", '', 11)
    pdf.cell(150, 8, sample_id_clean, 0, 1)

    pdf.line(10, 55, 200, 55)
    pdf.ln(5)

    # 3. Diagnosis
    pdf.set_font("Arial", 'B', 14)
    pdf.set_text_color(180, 0, 0) # لون أحمر داكن لاسم البكتيريا
    pdf.cell(0, 10, f"Isolated Pathogen: {bacteria_name_clean}", ln=True)
    pdf.set_text_color(0, 0, 0)
    pdf.ln(5)

    # 4. AST Table Header
    pdf.set_font("Arial", 'B', 11)
    pdf.cell(20, 10, "Disk", 1, 0, 'C')
    pdf.cell(80, 10, "Antibiotic", 1, 0, 'C')
    pdf.cell(35, 10, "Zone (mm)", 1, 0, 'C')
    pdf.cell(55, 10, "Interpretation", 1, 1, 'C')

    # 5. AST Table Data
    pdf.set_font("Arial", '', 11)
    for row in report_data:
        disk_num = str(row['Disk Number']).replace("🟢", "").replace("🟡", "").replace("🔴", "").replace("🔵", "").replace("🟣", "").strip()
        disk_num = clean_text(disk_num)
        
        antibiotic_clean = clean_text(row['Antibiotic'])
        zone_clean = clean_text(row['Diameter (mm)'])
        
        pdf.cell(20, 10, disk_num, 1, 0, 'C')
        pdf.cell(80, 10, antibiotic_clean, 1, 0, 'C')
        pdf.cell(35, 10, zone_clean, 1, 0, 'C')
        
        status = str(row['Clinical Status'])
        
        # تنظيف النص من الإيموجيز قبل الطباعة
        clean_status = status.replace("✅", "").replace("❌", "").replace("⚠️", "").replace("⏳", "").strip()
        clean_status = clean_text(clean_status)
        
        # تلوين النتيجة (S أخضر، R أحمر، I أصفر داكن)
        if "Sensitive" in status:
            pdf.set_text_color(0, 128, 0)
        elif "Resistant" in status:
            pdf.set_text_color(255, 0, 0)
        else:
            pdf.set_text_color(204, 153, 0)
            
        # هنطبع النص النظيف 
        pdf.cell(55, 10, clean_status, 1, 1, 'C')
        pdf.set_text_color(0, 0, 0) # نرجع اللون أسود تاني

    # 6. Footer Disclaimer
    pdf.ln(15)
    pdf.set_font("Arial", 'I', 9)
    pdf.set_text_color(100, 100, 100)
    pdf.cell(0, 5, "This report was automatically generated by the BactoVision CDSS pipeline.", ln=True, align='C')
    pdf.cell(0, 5, "Please consult a clinical microbiologist or physician for final therapeutic decisions.", ln=True, align='C')

    # تحويل الـ PDF لـ bytes 
    return pdf.output(dest='S').encode('latin1')

