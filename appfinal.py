#دالة سحب الادوية 
def get_clinical_recommendations(detected_bacteria_list):
    recs = []
    for bac in detected_bacteria_list:
        if bac in ANTIBIOTIC_GUIDE:
            if "Recommended" in ANTIBIOTIC_GUIDE[bac]:
                # تجميع العائلات الموصى بها للـ P.aeruginosa
                drugs = []
                for family_drugs in ANTIBIOTIC_GUIDE[bac]["Recommended"].values():
                    drugs.extend(family_drugs)
                recs.append(f"[{bac}] Test Panel: {', '.join(drugs)}")
            elif "Phenotypes" in ANTIBIOTIC_GUIDE[bac]:
                # بناخد الأدوية الحساسة من أول حالة (زي הـ MSSA) كبداية مبدئية لحد ما يتعمل AST
                first_pheno = list(ANTIBIOTIC_GUIDE[bac]["Phenotypes"].keys())[0]
                drugs = ANTIBIOTIC_GUIDE[bac]["Phenotypes"][first_pheno].get("✅ Sensitive (S)", [])
                recs.append(f"[{bac}] Primary Panel: {', '.join(drugs)}")
    return " | ".join(recs) if recs else "No baseline data"

